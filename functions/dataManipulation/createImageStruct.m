function  imageStruct = createImageStruct( redPathArray , yelPathArray )
%CREATECONDITIONS create empty structs for each conditions as listed in
%'conditionsStr'

pathN = length(redPathArray);
imageTemplate = struct(...
			'redPath',''...
			,'yelPath',''...
			,'boundingBox',[] ...
			,'cellWidth',[] ...
			,'cellLength',[] ...
			,'meanInsideCell',[]...
			,'meanOutsideCell',[]...
			,'meanMembrane',[]...
			,'cellN',[] ...
			);

for i=1:pathN
	imageStruct(i,1) = imageTemplate;
	imageStruct(i).redPath = redPathArray{i};
	imageStruct(i).yelPath = yelPathArray{i};
end

end

